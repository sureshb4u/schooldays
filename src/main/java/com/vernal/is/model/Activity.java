package com.vernal.is.model;

/**
 * @author bashelu
 *
 */
public class Activity {

	private String userName;
	private String status;
	private String typeOfActivity;
	private String employeeName;
	private String activityDateTime;
	/**
	 * @return the userName
	 */
	public String getUserName() {
		return userName;
	}
	/**
	 * @param userName the userName to set
	 */
	public void setUserName(String userName) {
		this.userName = userName;
	}
	/**
	 * @return the status
	 */
	public String getStatus() {
		return status;
	}
	/**
	 * @param status the status to set
	 */
	public void setStatus(String status) {
		this.status = status;
	}
	/**
	 * @return the typeOfActivity
	 */
	public String getTypeOfActivity() {
		return typeOfActivity;
	}
	/**
	 * @param typeOfActivity the typeOfActivity to set
	 */
	public void setTypeOfActivity(String typeOfActivity) {
		this.typeOfActivity = typeOfActivity;
	}
	/**
	 * @return the employeeName
	 */
	public String getEmployeeName() {
		return employeeName;
	}
	/**
	 * @param employeeName the employeeName to set
	 */
	public void setEmployeeName(String employeeName) {
		this.employeeName = employeeName;
	}
	/**
	 * @return the activityDateTime
	 */
	public String getActivityDateTime() {
		return activityDateTime;
	}
	/**
	 * @param activityDateTime the activityDateTime to set
	 */
	public void setActivityDateTime(String activityDateTime) {
		this.activityDateTime = activityDateTime;
	}
	
	
}
